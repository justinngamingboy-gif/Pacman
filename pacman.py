            for col in row:
                if col == 0:
                    print(" ")  
                if col == 1:
                    x1 = self.cell_size * x
                    y1 = self.cell_size * y 
                    x2 = x1 + self.cell_size
                    y2 = y1 + self.cell_size
                    self.canvas.create_rectangle(x1, y1, x2, y2, fill="blue", outline="white")
